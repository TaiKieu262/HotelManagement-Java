    // Hàm hỗ trợ: Tải dữ liệu từ Database lên bảng
    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Xóa hết dữ liệu cũ trên bảng
        
        ServiceDAO dao = new ServiceDAO();
        List<Service> list = dao.getAll();
        for (Service s : list) {
            model.addRow(new Object[]{ s.getMaDV(), s.getTenDV(), s.getGiaTienHienThi() });
        }
    }

    private void themActionPerformed(java.awt.event.ActionEvent evt) {                                     
    // 1. Lấy model của bảng (JTable) để thao tác dữ liệu
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // 2. Kiểm tra dữ liệu đầu vào (không để trống)
    if (txttendv.getText().trim().isEmpty() || txtgiatien.getText().trim().isEmpty() || txtmadv.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ Mã, Tên và Giá!");
        return;
    }

    try {
        
        // Nếu thành công, thực hiện thêm vào bảng
        // Dùng String.format("%.0f", ...) để hiển thị số nguyên, loại bỏ chữ E và số thập phân thừa
        Service sv = new Service(txtmadv.getText().trim(), txttendv.getText().trim(), Double.parseDouble(txtgiatien.getText().trim().replace(",", "")));
        
        ServiceDAO dao = new ServiceDAO();
        if (dao.insert(sv)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thành công!");
            loadData(); // Tải lại bảng từ CSDL
            // 4. Xóa trắng các ô nhập sau khi thêm thành công
            txtmadv.setText("");
            txttendv.setText("");
            txtgiatien.setText("");
            txtmadv.requestFocus();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại! Có thể trùng mã.");
        }
    } catch (NumberFormatException e) {
        // Nếu người dùng nhập sai (ví dụ nhập "abc" vào ô giá)
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: Giá tiền phải là con số!");
        txtgiatien.requestFocus(); // Đưa con trỏ chuột về lại ô giá để nhập lại
    }
    }                                    


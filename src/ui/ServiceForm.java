    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {                                    
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectedRow = jTable1.getSelectedRow(); // Lấy dòng đang được chọn
    
    if (selectedRow != -1) {
        String maDV = jTable1.getValueAt(selectedRow, 0).toString();
        ServiceDAO dao = new ServiceDAO();
        if (dao.delete(maDV)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadData(); // Tải lại bảng
            txtmadv.setText("");
            txttendv.setText("");
            txtgiatien.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn một dịch vụ để xóa!");
    }
    }                                   

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
    int row = jTable1.getSelectedRow();
    txtmadv.setText(jTable1.getValueAt(row, 0).toString());
    txttendv.setText(jTable1.getValueAt(row, 1).toString());
    txtgiatien.setText(jTable1.getValueAt(row, 2).toString());
    }                                    

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {                                    
    int row = jTable1.getSelectedRow();
    
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
        return;
    }

    // 1. Kiểm tra dữ liệu không được để trống (tránh lỗi lưu ô trống vào bảng)
    if (txttendv.getText().trim().isEmpty() || txtgiatien.getText().trim().isEmpty() || txtmadv.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
        return;
    }

    try {
        // 2. Format lại giá tiền cho đồng bộ (tránh lỗi hiển thị 1.0E7)
        // KHỞI TẠO ĐỐI TƯỢNG SERVICE
        Service sv = new Service(txtmadv.getText().trim(), txttendv.getText().trim(), Double.parseDouble(txtgiatien.getText().trim().replace(",", "")));
        
        ServiceDAO dao = new ServiceDAO();
        if (dao.update(sv)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thành công!");
            loadData(); // Tải lại bảng
            // 3. Xóa trắng các ô nhập liệu sau khi sửa xong
            txtmadv.setText("");
            txttendv.setText("");
            txtgiatien.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi: Giá tiền không hợp lệ!");
    }
    }                                   


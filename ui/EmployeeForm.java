    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String keyword = txtSearch.getText().trim();
    
    // Nếu để trống thì load lại toàn bộ bảng
    if (keyword.isEmpty()) {
        loadEmployeeTable();
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ trên bảng để hiển thị kết quả lọc

   EmployeeDAO dao = new EmployeeDAO();
    List<Employee> list = dao.searchAllFields(keyword); // Dùng hàm mới ở trên

    if (list.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên nào có tên: " + keyword);
    } else {
        for (Employee e : list) {
            model.addRow(new Object[]{
                e.getId(),
                e.getName(),
                e.getPhone(),
                e.getEmail(),
                e.getPosition()
            });
        }
    }
    
    
    

    }//GEN-LAST:event_btnSearchActionPerformed


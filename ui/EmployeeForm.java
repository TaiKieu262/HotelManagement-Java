    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       // 1. Lấy dữ liệu từ giao diện
    String idStr = txtId.getText().trim();
    String name = txtName.getText().trim();
    String phone = txtPhone.getText().trim();
    String email = txtEmail.getText().trim();
    String position = txtPosition.getText().trim();

    // 2. BẮT LỖI TRỐNG DỮ LIỆU (Phải nằm trên cùng)
    if (idStr.isEmpty() || name.isEmpty() || phone.isEmpty() || email.isEmpty() || position.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tất cả các trường dữ liệu!");
        return; // Dừng lại không thực hiện thêm
    }

    try {
        int id = Integer.parseInt(idStr);
        EmployeeDAO dao = new EmployeeDAO();

        // 3. BẮT LỖI TRÙNG ID (Chỉ chạy khi dữ liệu đã đủ)
        if (dao.checkDuplicateId(id)) {
            JOptionPane.showMessageDialog(this, "Lỗi: ID " + id + " đã tồn tại. Vui lòng nhập ID khác!");
            return; // Dừng lại không thực hiện thêm
        }

        // 4. THỰC HIỆN THÊM KHI MỌI THỨ ĐÃ HỢP LỆ
        Employee emp = new Employee();
        emp.setId(id);
        emp.setName(name);
        emp.setPhone(phone);
        emp.setEmail(email);
        emp.setPosition(position);

        if (dao.insert(emp)) {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
            loadEmployeeTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại (Lỗi Database)!");
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID phải là số nguyên!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
// 1. Lấy dữ liệu và dùng .trim() để loại bỏ khoảng trắng dư thừa
    String idText = txtId.getText().trim();
    String name = txtName.getText().trim();
    String phone = txtPhone.getText().trim();
    String email = txtEmail.getText().trim();
    String position = txtPosition.getText().trim();

    // 2. KIỂM TRA ĐIỀU KIỆN (Đây là phần bạn đang thiếu)
    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một nhân viên từ bảng để cập nhật!");
        return; // Dừng hàm, không chạy tiếp
    }

    if (name.isEmpty() || phone.isEmpty() || email.isEmpty() || position.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Không được để trống bất kỳ trường dữ liệu nào!");
        return; // Dừng hàm nếu có ô trống
    }

    // 3. Nếu mọi thứ ổn mới bắt đầu gọi DAO để cập nhật
    try {
        Employee emp = new Employee();
        emp.setId(Integer.parseInt(idText));
        emp.setName(name);
        emp.setPhone(phone);
        emp.setEmail(email);
        emp.setPosition(position);

        EmployeeDAO dao = new EmployeeDAO();
        if (dao.update(emp)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadEmployeeTable(); 
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại! ID có thể không tồn tại.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
    }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        String idText = txtId.getText().trim();

        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên để xóa");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Bạn có chắc muốn xóa nhân viên này?",
            "Xác nhận",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            int id = Integer.parseInt(idText);
            EmployeeDAO dao = new EmployeeDAO();

            if (dao.deleteById(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                loadEmployeeTable();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên hoặc lỗi xóa");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID không hợp lệ");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

